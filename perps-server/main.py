def main():
    """Main entry point"""
    is_api = os.getenv('IS_API', '0') == '1'
    is_worker = os.getenv('IS_WORKER', '0') == '1'
    
    # Default: Worker mode if nothing specified
    if not is_api and not is_worker:
        is_worker = True
        print("‚ö†Ô∏è No mode specified, defaulting to WORKER mode")
    
    if is_api and is_worker:
        # Both modes
        try:
            asyncio.run(run_both_modes())
        except KeyboardInterrupt:
            print("\n‚úÖ Stopped")
    elif is_api:
        # API server only
        try:
            run_api_server()
        except KeyboardInterrupt:
            print("\n‚úÖ API Server stopped")
    elif is_worker:
        # Worker only
        try:
            asyncio.run(run_hedging_worker())
        except KeyboardInterrupt:
            print("\n‚úÖ Worker stopped")


if __name__ == "__main__":
    main()

